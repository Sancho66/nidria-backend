"""Section-types + field-preset catalog (samples phase B) — THE back-side
source of truth.

The 11 ready-made sections and their field presets, with labels (and
select options) in the 6 product languages. Mirrored from the frontend
catalog (nidria-frontend/src/lib/sectionTemplates.ts + fieldCatalog.ts +
locales/*/catalog.json — pointer documented there); the BACK copy is the
reference: the sample seed instantiates sections/fields from it, and
clone_template materializes the missing custom_field_definition rows of
the cloning agency from it (label in the agency language + full
label_i18n; options in the agency language — the definition model has no
per-language options).

Native fields (COLLECTABLE_BASE_FIELDS columns) attach as kind=base_field
and need no definition; every other key attaches as kind=custom_field."""

